package edu.adelino.primeirasemana;

public class Operadores {
    public static void main(String[] args) {

        //OPERADORES ARITMETICOS
        /*
            +  => adição
            -  => subtração
            *  => multiplicação
            /  => divisão
            %  => modulo
            ++ => incremento
            -- => decremento
         */

        //OPERADORES RELACIONAIS
        /*
            ==  => igual a
            !=  => diferente de
            >   => maior que
            <   => menor que
            >=  => maior ou igual que
            <=  => menor ou igual que
         */

        //OPERADORES LOGICOS
        /*
            &  => and (E)
            |  => or (OU)
            !  => not (negação)
            ^  => xor
            ||  => or curto circuito (OU)
            &&  => and curto circuito (E)
         */

        //OPERADORES TERNARIO
        /*
            (condição) ? valor_se_verdadeiro : valor_se_falso
            Ex.:
            (2 > 4 ) ? 50 : 80  -> resposta: 80
         */
    }
}
